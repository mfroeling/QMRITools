OpacityPlot[data_,minmax_,label_,ps_,col_,autosc_,legend_,frame_,flip_,op_,ccolor_,aspect_]:=
Module[{p1,p2,pdat1,pdat2,a,b,lab, plot,dimx,dimy,size,ratio},
	
