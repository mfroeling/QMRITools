	(*add label*)
	lab = LabelJoin[label];
	plot = Labeli[lab,ps,plot];
	
	(*add legend*)
	If[legend, Legendi[plot, col[[b]], minmax[[b,1]],minmax[[b,2]], size[[2]]], plot]
	]


